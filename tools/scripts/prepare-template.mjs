async function main() {
  let changed = 0;

  for (const [relPath, content] of Object.entries(templates)) {
    const absPath = path.join(root, relPath);
    let current = "";
    try {
      current = await fs.readFile(absPath, "utf8");
    } catch {
      // file doesn't exist, will create
    }

    if (current === content) {
      console.log(`  skip: ${relPath} (already template)`);
      continue;
    }

    if (dryRun) {
      console.log(`  would reset: ${relPath}`);
    } else {
      await fs.mkdir(path.dirname(absPath), { recursive: true });
      await fs.writeFile(absPath, content, "utf8");
      console.log(`  reset: ${relPath}`);
    }
    changed++;
  }

  if (changed === 0) {
    console.log("prepare-template: nothing to reset");
  } else if (dryRun) {
    console.log(`prepare-template: ${changed} file(s) would be reset (dry-run)`);
  } else {
    console.log(`prepare-template: ${changed} file(s) reset`);
  }

  return changed;
}

main().catch((err) => {
  console.error(`prepare-template: failed: ${err.message}`);
  process.exit(1);
});
